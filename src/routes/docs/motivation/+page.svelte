	<h1 class="font-display mb-12 leading-[4.5rem] text-[5.75rem]">MOTIVATION</h1>
	<a
		class="inline-block mb-6 transition hover:translate-y-0.5"
		href="/docs/Motivation-shcherbakov-aleksandr.pdf"
		data-sveltekit-reload>Download as PDF ↓</a
