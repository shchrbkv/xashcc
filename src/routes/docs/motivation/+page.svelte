	<div class="text-stone-300/90 md:tracking-[0.01rem] md:leading-[1.8rem]">
		<p class="pb-6">
			In my first year of school, we moved from a remote taiga village to the city suburbs. Although I finally got
			access to the famous «internet», the only available ISP could not provide a reliable connection for any
			substantial download. As a result, I felt left behind when my inner-city peers discussed movies they streamed
			or online video games they played. This cultural alienation has ultimately led me to the idea — <i
				class="not-italic text-white md:tracking-[0.01rem]">if you can't use something, you create it yourself.</i
			>
		</p>
		<p class="pb-6">
			Over the next few years, I have spent countless hours downloading software to a school's internet terminal and
			transferring it to my home PC on a thumb drive. Tutorial by tutorial, I have become familiar with many
			professional tools. My passion resulted in dozens of 3D animations, website designs for local businesses, a
			visual identity for a volunteer organization and much more. It was mesmerizing to see how <i
				class="not-italic text-white md:tracking-[0.01rem]"
				>something I create digitally makes an impact on the real world.</i
			>
		</p>
		<p class="pb-6">
			However, the turning point for me was the wide adoption of smartphones. I witnessed how computers transitioned
			from being professional tools to essential personal companions. Access to the internet became so effortless
			that mobile computing became <i class="not-italic text-white md:tracking-[0.01rem]"
				>not just a part, but a whole digital extension of our life</i
			>. Seeing how intuitive, interactive and fluid touch interfaces can be, I naturally
			<i class="not-italic text-white md:tracking-[0.01rem]">wanted to become a part of this revolution.</i>
		</p>
		<p class="pb-6">
			Since then, I have been committed to building mobile interfaces with <i
				class="not-italic text-white md:tracking-[0.01rem]">outstanding user experience.</i
			>
			Taught by years of limitations, I have always strived for efficiency and wanted user interaction to feel responsive.
			This desire also motivated me to take up competitive programming in high school. As a result, I have acquired a
			strong foundation in computer science, with my team
			<i class="not-italic text-white md:tracking-[0.01rem]">placed second in the Siberian ICPC finals.</i>
			My hard work paid off when I have passed the national CS exam with a maximum score,
			<i class="not-italic text-white md:tracking-[0.01rem]">an achievement obtained by only 0.36% of graduates</i>,
			thus securing my place at ITMO University on a government-funded basis.
		</p>
		<p class="pb-6">
			University streamlined my previous knowledge and allowed me to dive deep into complex aspects of <i
				class="not-italic text-white md:tracking-[0.01rem]"
				>algorithms, software engineering, computer networking, and entrepreneurship.</i
			>
			My dedication to learning has been proven many times over with high-merit government scholarships and a
			<i class="not-italic text-white md:tracking-[0.01rem]">bachelor degree with honours</i>. I have had an
			opportunity to work on outstanding projects in a variety of roles, lead teams during hackathons and
			internships, help establish distance-learning processes and serve as an advisor for joint university
			communities.
		</p>
		<p class="pb-6">
			In addition to my studies, <i class="not-italic text-white md:tracking-[0.01rem]"
				>I have co-founded a mobile development team</i
			>
			with my EU-based business partner. We provided small enterprises with cross-platform applications that increased
			their reach and sales, especially during the pandemic. Although it was often challenging to maintain a high quality
			of work and studies, this combination of education and business provided me with priceless
			<i class="not-italic text-white md:tracking-[0.01rem]">decision-making, time management and teamwork skills</i
			>, which I find crucial to succeeding in any competitive environment.
		</p>
		<p class="pb-6">
			Today, the landscape for modern application development seems to have settled, though I refuse to believe that
			a 5-inch brick of glass and metal is its endpoint. Just as it once was with smartphones, it now feels like we
			are on the verge of the next generation of interfaces. <i class="not-italic text-white md:tracking-[0.01rem]"
				>Specifically spatial ones.</i
			>
		</p>
		<p class="pb-6">
			My ultimate goal is to research and build <i class="not-italic text-white md:tracking-[0.01rem]"
				>ambient computing systems which seamlessly enhance user experience</i
			>. I am interested in how computers can escape the limiting box of a screen and become a natural part of our
			lives — <i class="not-italic text-white md:tracking-[0.01rem]">inclusive, sustainable and effortless</i>. I am
			convinced that the HCI program is the perfect place to become a versatile developer capable of leveraging the
			latest achievements in XR, IoT, and ML to build
			<i class="not-italic text-white md:tracking-[0.01rem]">the foundation for future innovation.</i>
		</p>
		<p class="pb-6">
			I am particularly interested in the program's curriculum — with courses in <i
				class="not-italic text-white md:tracking-[0.01rem]"
				>human-computer interaction, machine learning and mobile networking,</i
			>
			it covers all the building blocks for the digital environments of tomorrow. Moreover, I am excited to collaborate
			on projects for <i class="not-italic text-white md:tracking-[0.01rem]">CHI, SIGGRAPH and other conferences</i>.
			Immersion in an international and diverse community of interdisciplinary researchers will enable me to
			<i class="not-italic text-white md:tracking-[0.01rem]"
				>make well-informed contribution to this rapidly-growing field.</i
			>
		</p>
		<p class="pb-6">
			In addition to academic potential, I am excited about using cutting-edge facilities such as Aalto Studios to <i
				class="not-italic text-white md:tracking-[0.01rem]"
				>develop projects at the forefront of human-computer interfaces.</i
			>
			I have been a longtime collaborator on 3D motion graphics with a large YouTube channel, therefore the prospect of
			<i class="not-italic text-white md:tracking-[0.01rem]"
				>applying my visual skills in an advanced production environment</i
			>
			is deeply inspiring.
		</p>
		<p class="pb-6">
			Overall, the opportunity to be a part of your program sets high standards for me as a student and drives me to
			bring the most innovative ideas to life. Including the ultimate one — <i
				class="not-italic text-white md:tracking-[0.01rem]"
				>to become a highly-skilled professional that my future university will be proud of.</i
			>
		</p>
		<p class="pb-6">
			<i class="not-italic text-white md:tracking-[0.01rem]">Looking forward to a prosperous cooperation!</i>
		</p>
		<p>Sincerely,<br />Aleksandr Shcherbakov</p>
